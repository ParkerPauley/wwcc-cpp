#include "util.h"

int main()
{
  
  char op;
  float temp;

    cout << "Temperature Converter    ";

    cout << "Enter Temp: ";
  cin >> temp;

  cout << "F or C? ";
  cin >> op;
    
    
    
 switch(op) {

    case 'F':
      cout << temp << " degrees Farenheit in Celsius is: " << (temp-32)*5/9;
      break;
    
    case 'C':
      cout << temp << " degrees Celsius in Farenheit is: " << (temp*9/5)+32;
      break;
 }
}
